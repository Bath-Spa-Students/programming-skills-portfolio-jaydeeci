# Define a name with leading and trailing whitespace
name = "\t \n   Ifrah Imtiaz \t \n"

# Print the name with the surrounding whitespace
print("Name with Whitespace:")
print(name)

# Use the lstrip() function to remove leading whitespace
print("\nUsing lstrip():")
print(name.lstrip())

# Use the rstrip() function to remove trailing whitespace
print("\nUsing rstrip():")
print(name.rstrip())

# Use the strip() function to remove both leading and trailing whitespace
print("\nUsing strip():")
print(name.strip())


